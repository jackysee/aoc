var map = data().split(/\n/).map(l => l.split(''));
var width = map[0].length;
var height = map.length;
var at = (x,y) => map[y][x % width];

function trees(dx, dy) {
  var step = [0,0];
  var points = [];
  while(step[1] < height) {
    points.push(at(...step));
    step[0] += dx;
    step[1] += dy;
  }
  return points.filter(n => n==='#').length;
}
