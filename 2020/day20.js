function parse(str) {
    return str.split('\n\n').map(t => {
        const arr = t.split('\n');
        const id = parseInt(arr[0].match(/\d+/)[0], 10);
        const tile = arr.slice(1).map(l => l.split(''));
        return { id, tile };
    });
}

function getEdges(tile) {
    const top = tile[0].join('');
    const bottom = tile[9].join('');
    const left = tile.map(l => l[0]).join('');
    const right = tile.map(l => l[9]).join('');
    return {top, bottom, left, right};
}

function orient(tile , ori = 1) {
    let len = tile[0].length;
    let _tile = [];
    for(let i=0; i<len; i++) {
        _tile.push([]);
        for(let j=0; j<len; j++) {
            let p = tile[i][j];
            if(ori === 2) { p = tile[len - 1 - j][i]; }
            if(ori === 3) { p = tile[len - 1 - i][len - 1 - j]; }
            if(ori === 4) { p = tile[j][len - 1 - i]; }
            if(ori === 5) { p = tile[i][len - 1 - j]; }
            if(ori === 6) { p = tile[len - 1 - j][len - 1 - i]; }
            if(ori === 7) { p = tile[len - 1 - i][j]; }
            if(ori === 8) { p = tile[j][i]; }
            _tile[i].push(p);
        }
    }
    // console.log(_tile);
    return _tile;
}

function render(_tile) {
    return _tile.map(l => l.join('')).join('\n');
}

function getEdgePairs(tile) {
    let result = [];
    for(let i=1; i<=8; i++) {
        let _tile = orient(tile, i);
        let { top,bottom,left,right } = getEdges(_tile);
        result.push({
            ori: i,
            edges: [
                [left, top],
                [top, right],
                [right, bottom],
                [bottom, left]
            ]
        });
    }
    return result;
}

