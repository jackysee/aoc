function getAllEdges(tile) {
    let result = [];
    for(let i=1; i<=8; i++) {
        let _tile = orient(tile, i);
        let edges = getEdges(_tile);
        result = [...result, ...Object.values(edges)];
    }
    return result;
}

function findCorners(tiles) {
    let corners = [];
    for(let i=0; i<tiles.length; i++) {
        let edgePairs = getEdgePairs(tiles[i].tile);
        let info;
        let isCorner = edgePairs.some(p => {
            let allEdges = tiles.filter(t => t.id !==  tiles[i].id).flatMap(t => getAllEdges(t.tile))
            let index = p.edges.findIndex(([e1, e2]) => !allEdges.includes(e1) && !allEdges.includes(e2));
            return index !== -1;
        });
        if(isCorner) {
            corners.push(tiles[i]);
        }
    }
    return corners;
}

