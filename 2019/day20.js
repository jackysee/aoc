function getNeighbors(G, _name) {
    let [name,inout,level] = _name.split(':');
    level = parseInt(level, 10);
    let nodes = G[name+":"+inout]
        .filter(n => {
            if(n.name === 'AA' || n.name === 'ZZ') {
                console.log(level, n);
            }
            if(level === 0) {
                return n.inner || /(AA|ZZ)/.test(n.name);
            }
            if(level > 0) {
                return !/(AA|ZZ)/.test(n.name);
            }
            return true;
        })
        .map(n => {
            let dl = n.inner? 1 : -1;
            let _inout = n.inner?'o':'i';
            if(/(AA|ZZ)/.test(n.name)) {
                _inout = 'o';
                dl = 0;
            }
            if(level + dl < 0) {
                console.log([name, inout, level], n.name);
            }
            return {
                name: [n.name, _inout, level + dl].join(':'),
                dist: n.dist
            }
        });
    return nodes;
}

function bfs(G, start = 'AA:o:0', end = 'ZZ:o:0') {
    let visited = new Set();
    let queue = [ { name:start, dist:0, trail:start } ];
    while(queue.length !== 0) {
        // if(queue.length % 10000 === 0) {
        //     console.log(queue.length);
        // }
        let node = queue.shift();
        if(node.name === end) {
            console.log('done::', node.trail);
            return node.dist - 1;
        }
        // console.log(visited);
        let nodes = getNeighbors(G, node.name)
            .filter(n => !visited.has(n.name))
            .map(n => ({ 
                ...n, 
                dist: node.dist + n.dist, 
                trail: `${node.trail}\n${n.name}(+${n.dist})`
            }))
        // console.log('getNeighbors', node.name, nodes);
        nodes.forEach(n => visited.add(n.name));
        queue = [...queue, ...nodes];
    }
    return -1;
}

