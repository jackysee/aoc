function ints(s) {
    return s.split(',').map(s => parseInt(s, 10));
}

function parse(str) {
    let arr = str.split('\n').map(l => l.split(''));
    let map = {};
    let keys = [];
    let doors = []; 
    let pos;
    for(let i=0; i< arr.length; i++) {
        for(let j=0; j<arr[i].length; j++) {
            let v = arr[i][j];
            map[[i,j]] = v;
            if(/[A-Z]/.test(v)) doors.push([i,j]);
            if(/[a-z]/.test(v)) keys.push([i,j]);
            if(v === '@') pos = [i,j]
        }
    }
    return { map, doors, keys, pos };
}

